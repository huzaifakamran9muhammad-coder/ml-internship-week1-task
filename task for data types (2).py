# Task 2 - Data Types

# Integer
age = 17

# Float
height = 5.9

# String
name = "Huzaifa"

# Boolean
is_student = True

# List
numbers = [1, 2, 3, 4, 5]

# Tuple
coordinates = (10, 20)

# Dictionary
student = {
    "name": "Huzaifa",
    "age": 20,
    "course": "ML Internship"
}

# Printing all data types
print("Integer:", age)
print("Float:", height)
print("String:", name)
print("Boolean:", is_student)
print("List:", numbers)
print("Tuple:", coordinates)
print("Dictionary:", student)

# Checking types
print(type(age))
print(type(height))
print(type(name))
